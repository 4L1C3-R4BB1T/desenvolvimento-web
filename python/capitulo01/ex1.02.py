numero_1 = float(input("Número 1: "))
numero_2 = float(input("Número 2: "))

soma = numero_1 + numero_2
subtracao = numero_1 - numero_2
multiplicacao = numero_1 * numero_2
divisao = numero_1 / numero_2

print(f"\nA soma de {numero_1} e {numero_2} é igual a {soma}")
print(f"A subtração de {numero_1} e {numero_2} é igual a {subtracao}")
print(f"A multiplicação de {numero_1} e {numero_2} é igual a {multiplicacao}")
print(f"A divisão de {numero_1} e {numero_2} é igual a {divisao}")
