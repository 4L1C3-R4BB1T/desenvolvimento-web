numero_1 = int(input("Número 1: "))
numero_2 = int(input("Número 2: "))
numero_3 = int(input("Número 3: "))

dobro_numero_1 = numero_1 * 2
dobro_numero_2 = numero_2 * 2
dobro_numero_3 = numero_3 * 2

soma = dobro_numero_1 + dobro_numero_2 + dobro_numero_3

print(f"\nA soma do dobro de {numero_1}, {numero_2} e {numero_3} é igual a {soma}.")
